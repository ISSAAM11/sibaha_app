abstract final class AppSpacing {
  // Extra small spacing
  static const xs = 4.0;

  // Small spacing
  static const sm = 8.0;

  // Small-medium spacing
  static const smMd = 10.0;

  // Medium spacing
  static const md = 12.0;

  // Medium-large spacing
  static const mdLg = 14.0;

  // Large spacing
  static const lg = 16.0;

  // Large-extra large spacing
  static const lgXl = 20.0;

  // Extra large spacing
  static const xl = 24.0;

  // Double extra large spacing
  static const xxl = 32.0;

  // Triple extra large spacing
  static const xxxl = 48.0;

  // Huge spacing
  static const huge = 64.0;
}
